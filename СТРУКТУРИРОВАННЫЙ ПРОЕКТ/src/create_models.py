texts = []
mbti_types = []
for text, mbti in base_data:
    for _ in range(10):
        texts.append(text)
        mbti_types.append(mbti)

def clean_text(txt):
    txt = txt.lower()
    txt = re.sub(r'[^a-zа-яё\s]', '', txt)
    return txt

texts_clean = [clean_text(t) for t in texts]

vec = TfidfVectorizer(token_pattern=r'[a-zа-яё]+', min_df=1)
X = vec.fit_transform(texts_clean)

y_ie = []
y_ns = []
y_tf = []
y_jp = []

for mbti in mbti_types:
    y_ie.append(1 if mbti[0] == 'I' else 0)
    y_ns.append(1 if mbti[1] == 'N' else 0)
    y_tf.append(1 if mbti[2] == 'T' else 0)
    y_jp.append(1 if mbti[3] == 'J' else 0)

models = {
    'IE': RandomForestClassifier(n_estimators=100, random_state=42).fit(X, np.array(y_ie)),
    'NS': RandomForestClassifier(n_estimators=100, random_state=42).fit(X, np.array(y_ns)),
    'TF': RandomForestClassifier(n_estimators=100, random_state=42).fit(X, np.array(y_tf)),
    'JP': RandomForestClassifier(n_estimators=100, random_state=42).fit(X, np.array(y_jp))
}

joblib.dump(models, 'models/rf_models.pkl')
joblib.dump(vec, 'models/vectorizer.pkl')

